private void loadingAnimation() {
        Thread t = new Thread(() -> {
            for (int i = 0; i <= 100; i++) {
                final int progress = i;
                SwingUtilities.invokeLater(()->{
                jProgressBar1.setValue(progress);

                if (progress < 30) {
                    jLabel2.setText("String module " + progress + "%");
                } else if (progress > 30 && progress < 50) {
                    jLabel2.setText("Database connection established... " +progress + "%");
                } else if (progress > 80) {
                    jLabel2.setText("Done " + progress + "%");
                }
                  });
                try {
                    Thread.sleep(40);      
} catch (InterruptedException e) {
                    e.printStackTrace();
                }
            }
 new LoginPage().setVisible(true);
            this.dispose();
        });

        t.start();
    }

   
